package com.upload.app.core.aop;

import com.upload.app.core.exception.XsvException;
import com.upload.app.core.util.JsonResult;
import org.springframework.core.annotation.Order;
import org.springframework.http.HttpStatus;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.bind.annotation.ResponseBody;
import org.springframework.web.bind.annotation.ResponseStatus;

/**
 * 全局的的异常拦截器（拦截所有的控制器）（带有@RequestMapping注解的方法上都会拦截）
 *
 * @author fengshuonan
 * @date 2016年11月12日 下午3:19:56
 */
@ControllerAdvice
@Order(-1)
public class GlobalExceptionHandler {

    @ExceptionHandler(XsvException.InvalidBitcoinAddressException.class)
    @ResponseStatus(HttpStatus.OK)
    @ResponseBody
    public JsonResult invalidBitcoinAddress(XsvException.InvalidBitcoinAddressException e) {
        return new JsonResult(e.getCode(), e.getMessage());
    }



}
